package primer_paquete;

public class Main {
    public static void main(String[] args) {
        // introduzco datos en variables
        int a = 57;
        long b = 922337203685L;
        double c = -8.74;
        boolean d = true;
        String e = "String";

        // imprimo por pantalla
        System.out.println("Tipo de dato entero: " + a );
        System.out.println("Tipo de dato entero (largo): " + b );
        System.out.println("Tipo de dato decimal: " + c );
        System.out.println("Tipo de dato Booleano: " + d );
        System.out.println("Tipo de dato String: " + e );
    }
}